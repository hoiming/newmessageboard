package com.haiming.messageboard.bean;

public class DateTimeInfo {
	private String info;

	public DateTimeInfo(String info) {
		super();
		this.info = info;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
	public String toString(){ 
		return info;
	}
	
	

}
